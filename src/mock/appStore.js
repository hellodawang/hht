function randomizeData(apps) {
    apps.forEach(v => {
        v.name = v.imgUrl.split('.')[0].split('/').pop()
        v.size = Math.ceil(Math.random() * 50 + 1) + 'M'
        v.rate = Math.ceil(Math.random() * 5) 
        v.downloadTimes = Math.ceil(Math.random() * 9000) 
    })
    apps.sort((a, b) => a.rate < b.rate ? 1 : -1)

    return apps
}

